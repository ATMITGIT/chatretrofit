package com.example.diplomprogect.models;

public class Chat {
    private String id;
    private String nickname, login;
    public Chat(String id, String nickname, String login) {
        this.id = id;
        this.nickname = nickname;
        this.login = login;
    }
    public String getId() {
        return id;
    }

    public String getNickname() {
        return nickname;
    }

    public String getLogin() {
        return login;
    }










    }


